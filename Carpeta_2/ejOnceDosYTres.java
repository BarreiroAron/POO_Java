package elvideodiseotracosa;

public class ejOnceDosYTres {
	public static void main(String[] args) {
		for(int i = 1; i <= 100; i++) {
			if(i % 2 == 0) {
			System.out.println("-----   2   -----");	
			System.out.println((i));
			System.out.println("-----       -----");	
			}
			if(i % 3 == 0) {
				System.out.println("-----   3   -----");	
				System.out.println((i));
				System.out.println("-----       -----");	
			}
		}
	}
}
